// ------------------------------------------------
function getСomparison(a, b) {
  if (typeof a !== 'number' || typeof b !== 'number') {
    return 'Not a number';
  }
  if (a > b) {
    return b;
  } else {
    return a;
  }
}
// ------------------------------------------------
function getСomparisons(c, d) {
  if (c !== Number(c) || d !== Number(d)) {
    return 'Not a number';
  }
  return c > d ? d : c;
}
